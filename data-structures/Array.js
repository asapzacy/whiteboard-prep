class Array {

  constructor() {
    this.array = []
  }

  /*  ------------------------------------------------------  */

  add(data) {
    this.array.push(data)
  }

  /*  ------------------------------------------------------  */

  remove(data) {
    this.array = this.array.filter(item => item !== data)
  }

  /*  ------------------------------------------------------  */

  search(data) {
    return this.array.includes(data) ? this.array.indexOf(data) : null
  }

  /*  ------------------------------------------------------  */

  getAtIndex(index) {
    return this.array[index]
  }

  /*  ------------------------------------------------------  */

  size() {
    return this.array.length
  }

  /*  ------------------------------------------------------  */

  print() {
    console.log(this.array.join(' '))
  }

}


/*  ------------------------------------------------------  */
/*  ------------------------------------------------------  */


const a = new Array()
a.add(1)
a.add(23)
a.add(99)
a.add(100)
a.add('hi')
console.log(a)


