const findComplement = (x) => {
  const bin = x.toString(2).split('')
  for (let i = 0; i < bin.length; i++) {
    bin[i] = 1 - bin[i]
  }
  return parseInt(bin.join(''), 2)
}


/*  ------------------------------------------------------  */
/*  ------------------------------------------------------  */

const assert = require('assert')

assert.equal(findComplement(5), 2)
assert.equal(findComplement(1), 0)

