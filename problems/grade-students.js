const gradeStudents = (arr) => {
  let result = []
  for (let i = 0; i < arr.length; i++) {
    let item = arr[i]
    if ((item % 5) > 2) {
      if (item > 37) {
        item = Math.ceil(item / 5) * 5
      }
    }
    result.push(item)
  }
  return result
}


/*  ------------------------------------------------------  */
/*  ------------------------------------------------------  */

const assert = require('assert')

assert.deepEqual(gradeStudents([73,67,38,33]), [75,67,40,33])
assert.deepEqual(gradeStudents([99,94,69,56]), [100,95,70,56])

