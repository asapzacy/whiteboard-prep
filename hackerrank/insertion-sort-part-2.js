const insertionSortP2 = (arr) => {
  for (let i = 1; i < arr.length; i++) {
    let temp = arr[i]
    let j = i - 1
    while (j >= 0 && arr[j] > temp) {
      arr[j + 1] = arr[j]
      j -= 1
    }
    arr[j + 1] = temp
  }
  return arr
}


/*  ------------------------------------------------------  */
/*  ------------------------------------------------------  */

const assert = require('assert')

assert.deepEqual(insertionSortP2([1,4,3,5,6,2]), [1,2,3,4,5,6])

